"""
ChartMimic semantic evaluation wrapper.

Delegates to ChartMimic's official evaluators (TextEvaluator, ChartTypeEvaluator,
ColorEvaluator, LayoutEvaluator) via a subprocess that sets up the correct
working directory and environment required by those evaluators.

The evaluators need:
  - CWD = CHARTMIMIC_ROOT  (so os.system("python3 python_wrapper.py ...") works)
  - PROJECT_PATH env var = CHARTMIMIC_ROOT   (for eval_configs and chart2code imports)
  - sys.path includes CHARTMIMIC_ROOT        (namespace-package imports)
  - A Python that has matplotlib, dotenv, etc. installed → we use ChartMimic/.venv

Returns per-sample metric dicts:
  {
    "exec_ok":        bool,
    "text_f1":        float,   # text/label F1 (TextEvaluator, use_axs=False)
    "chart_type_f1":  float,   # chart-type F1 (ChartTypeEvaluator)
    "color_f1":       float,   # color palette F1 (ColorEvaluator)
    "layout_f1":      float,   # layout grid F1 (LayoutEvaluator)
  }
"""

from __future__ import annotations

import json
import logging
import os
import subprocess
import sys
import tempfile
from pathlib import Path

# ChartMimic root — relative to this file's location in eval/
_HERE = Path(__file__).resolve().parent
_PROJECT_ROOT = _HERE.parent
CHARTMIMIC_ROOT = str(_PROJECT_ROOT / "ChartMimic")

# Python interpreter for the ChartMimic evaluator subprocess.
# Prefer ChartMimic/.venv if it exists (has matplotlib, dotenv, etc.);
# otherwise fall back to the current interpreter.
_CHARTMIMIC_PYTHON = str(_PROJECT_ROOT / "ChartMimic" / ".venv" / "bin" / "python")
if not os.path.isfile(_CHARTMIMIC_PYTHON):
    _CHARTMIMIC_PYTHON = sys.executable

# ---------------------------------------------------------------------------
# Inline eval script — embedded as a template string, formatted at call time
# ---------------------------------------------------------------------------
# NOTE: double-brace {{ }} for literal braces in the formatted output.
_EVAL_SCRIPT_TEMPLATE = """\
import os, sys, json

# Set up the ChartMimic execution environment
os.chdir({root!r})
os.environ['PROJECT_PATH'] = {root!r}
sys.path.insert(0, {root!r})                  # for eval_configs, chart2code (namespace pkg)
sys.path.insert(0, os.path.join({root!r}, 'chart2code'))  # for utils.evaluator.* direct imports

import warnings
warnings.filterwarnings('ignore')

# ChartMimic's text evaluator uses eval() to parse extracted text data.
# With matplotlib 3.8+, the output contains np.float64(...) which requires
# numpy in the eval namespace. Monkey-patch builtins so eval() can resolve np.
import numpy as np
import builtins
builtins.np = np

from utils.evaluator.text_evaluator import TextEvaluator
from utils.evaluator.chart_type_evaluator import ChartTypeEvaluator
from utils.evaluator.color_evaluator import ColorEvaluator
from utils.evaluator.layout_evaluator import LayoutEvaluator

gen_file = {gen_file!r}
gt_file  = {gt_file!r}

text_e       = TextEvaluator(use_position=False, use_axs=False)
chart_type_e = ChartTypeEvaluator()
color_e      = ColorEvaluator()
layout_e     = LayoutEvaluator()

exec_ok = True
try:
    text_e(gen_file, gt_file)
    chart_type_e(gen_file, gt_file)
    color_e(gen_file, gt_file)
    layout_e(gen_file, gt_file)
except Exception as exc:
    sys.stderr.write(f"Evaluator error: {{exc}}\\n")
    exec_ok = False

print(json.dumps({{
    "exec_ok":       exec_ok,
    "text_f1":       text_e.metrics["f1"],
    "chart_type_f1": chart_type_e.metrics["f1"],
    "color_f1":      color_e.metrics["f1"],
    "layout_f1":     layout_e.metrics["f1"],
}}))
"""


def evaluate(
    generated_code: str,
    gt_py_path: str,
    timeout: int = 300,
) -> dict:
    """
    Run ChartMimic semantic evaluation for one sample.

    Parameters
    ----------
    generated_code : str
        The Python/Matplotlib code generated by the actor.  May include
        markdown fences; they are stripped before writing to disk.
    gt_py_path : str
        Path to the ground-truth .py file from the ChartMimic dataset.
        Can be absolute or relative to project root.
    timeout : int
        Subprocess timeout in seconds.

    Returns
    -------
    dict
        Keys: exec_ok (bool), text_f1, chart_type_f1, color_f1, layout_f1 (floats).
        All metrics are 0.0 / False on any failure.
    """
    # Resolve gt_py_path — it may be stored as a relative path like
    # "ChartMimic/dataset/direct_600/bar_3.py"
    gt_path = Path(gt_py_path)
    if not gt_path.is_absolute():
        gt_path = _PROJECT_ROOT / gt_path
    if not gt_path.exists():
        logging.debug("GT file does not exist: %s", gt_path)
        return _empty_metrics()

    # Strip markdown fences from generated code before writing to disk
    import re
    code = re.sub(r"```(?:python)?\s*\n?([\s\S]*?)```", r"\1", generated_code)

    # Write generated code to a temporary .py file
    gen_fd, gen_path = tempfile.mkstemp(suffix=".py")
    try:
        with os.fdopen(gen_fd, "w", encoding="utf-8") as f:
            f.write(code)

        script = _EVAL_SCRIPT_TEMPLATE.format(
            root=CHARTMIMIC_ROOT,
            gen_file=gen_path,
            gt_file=str(gt_path),
        )

        env = os.environ.copy()
        env["PROJECT_PATH"] = CHARTMIMIC_ROOT
        env["MPLBACKEND"] = "Agg"
        # Evaluators internally call `os.system("python3 python_wrapper.py ...")`
        # — ensure `python3` resolves to the ChartMimic venv's python
        venv_bin = str(Path(_CHARTMIMIC_PYTHON).parent)
        env["PATH"] = venv_bin + os.pathsep + env.get("PATH", "")

        # The ChartMimic evaluators internally call os.system("python3 ...")
        # which spawns grandchild processes. Using capture_output=True (PIPE)
        # can cause pipe buffer deadlocks when those grandchildren produce
        # output. Instead, capture stdout to a temp file and discard stderr.
        stdout_fd, stdout_path = tempfile.mkstemp(suffix="_eval_stdout.txt")
        os.close(stdout_fd)
        try:
            with open(stdout_path, "w") as stdout_f:
                result = subprocess.run(
                    [_CHARTMIMIC_PYTHON, "-c", script],
                    stdout=stdout_f,
                    stderr=subprocess.DEVNULL,
                    timeout=timeout,
                    cwd=CHARTMIMIC_ROOT,
                    env=env,
                )

            if result.returncode != 0:
                logging.debug(
                    "ChartMimic evaluator subprocess failed (rc=%d)",
                    result.returncode,
                )
                return _empty_metrics()

            # Parse JSON from last non-empty stdout line
            with open(stdout_path) as f:
                lines = [ln.strip() for ln in f if ln.strip()]
            if not lines:
                logging.debug("ChartMimic evaluator: empty stdout")
                return _empty_metrics()

            metrics = json.loads(lines[-1])
            return metrics
        finally:
            try:
                os.unlink(stdout_path)
            except OSError:
                pass

    except subprocess.TimeoutExpired:
        logging.debug("ChartMimic evaluator timed out after %d s", timeout)
        return _empty_metrics()
    except json.JSONDecodeError as exc:
        logging.debug("ChartMimic evaluator: could not parse JSON: %s", exc)
        return _empty_metrics()
    except Exception as exc:
        logging.debug("ChartMimic evaluator: unexpected error: %s", exc)
        return _empty_metrics()
    finally:
        try:
            os.unlink(gen_path)
        except OSError:
            pass


def _empty_metrics() -> dict:
    return {
        "exec_ok":       False,
        "text_f1":       0.0,
        "chart_type_f1": 0.0,
        "color_f1":      0.0,
        "layout_f1":     0.0,
    }
